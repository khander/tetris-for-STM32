void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if(GPIO_Pin == btnTest_Pin){
		static uint8_t prev_val;
		if(prev_val == 0){
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, SET);
			prev_val = 1;
		} else {
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, RESET);
			prev_val = 0;
		}
	}
}
